// Класс CoupleMaker предназначен для объединения двух коллекций в одну мапу (словарь).
class CoupleMaker {
  // Статический метод для конвертации пары множеств (ключей и значений) в словарь.
  static Map<String, dynamic> CMtoMap(Set<String> keys, Set<dynamic> vals) {
    // Проверка длины множеств, ключи и значения должны иметь одинаковое количество элементов.
    if (keys.length != vals.length) {
      throw ArgumentError("Количество ключей должно совпадать с количеством значений."); // Исключение при несоответствии длин.
    }

    // Преобразование множеств в списки для последовательного перебора и гарантированного порядка.
    final keyList = keys.toList();
    final valList = vals.toList();

    // Создание нового пустого словаря.
    final map = <String, dynamic>{};

    // Цикл по списку ключей и заполнение словаря элементами из второго списка.
    for (var i = 0; i < keyList.length; i++) {
      map[keyList[i]] = valList[i]; // Каждому ключу присваиваем соответствующее значение.
    }

    return map; // Возвращаем сформированный словарь.
  }
}